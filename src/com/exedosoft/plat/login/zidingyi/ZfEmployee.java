package com.exedosoft.plat.login.zidingyi;



public class ZfEmployee extends com.exedosoft.plat.bo.BaseObject{
	
	private String uid;
	
	private String cn;
	
	private String sn;
	
	private String mail;
	

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getCn() {
		return cn;
	}

	public void setCn(String cn) {
		this.cn = cn;
	}

	public String getSn() {
		return sn;
	}

	public void setSn(String sn) {
		this.sn = sn;
	}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

}
